const fs = require('fs');
const path = require('path');

const BACKEND_ROOT = path.resolve(__dirname, '..');
const SOURCE_SCHEMA = path.join(BACKEND_ROOT, 'prisma', 'schema.prisma');
const GENERATED_DIR = path.join(BACKEND_ROOT, 'prisma', '.generated');
const OUTPUT_SCHEMA = path.join(GENERATED_DIR, 'schema.tidb.prisma');

const SQLITE_PROVIDER = 'provider = "sqlite"';
const MYSQL_PROVIDER = 'provider = "mysql"';
const FRONTEND_CLIENT_OUTPUT = 'output   = "../../frontend/next-app/node_modules/.prisma/client"';
const GENERATED_FRONTEND_CLIENT_OUTPUT = 'output   = "../../../frontend/next-app/node_modules/.prisma/client"';

function prepareTidbSchema() {
    let schema = fs.readFileSync(SOURCE_SCHEMA, 'utf8');

    if (!schema.includes(SQLITE_PROVIDER)) {
        throw new Error(`Could not find ${SQLITE_PROVIDER} in ${SOURCE_SCHEMA}`);
    }

    schema = schema.replace(SQLITE_PROVIDER, MYSQL_PROVIDER);

    if (schema.includes(FRONTEND_CLIENT_OUTPUT)) {
        schema = schema.replace(FRONTEND_CLIENT_OUTPUT, GENERATED_FRONTEND_CLIENT_OUTPUT);
    }

    const banner = [
        '// Generated by backend/scripts/prepare-tidb-prisma.js',
        '// Do not edit manually.',
        '',
    ].join('\n');

    fs.mkdirSync(GENERATED_DIR, { recursive: true });
    fs.writeFileSync(OUTPUT_SCHEMA, `${banner}${schema}`, 'utf8');

    return OUTPUT_SCHEMA;
}

if (require.main === module) {
    const outputPath = prepareTidbSchema();
    console.log(outputPath);
}

module.exports = {
    prepareTidbSchema,
    OUTPUT_SCHEMA,
};
